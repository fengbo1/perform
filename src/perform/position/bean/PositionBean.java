package perform.position.bean;

public class PositionBean {

	private String id;
	private String name;
	private String chu;
	private String tuan;
	private String kpip;
	private String ktip;
	private String kbip;
	private String kcip;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getChu() {
		return chu;
	}
	public void setChu(String chu) {
		this.chu = chu;
	}
	public String getTuan() {
		return tuan;
	}
	public void setTuan(String tuan) {
		this.tuan = tuan;
	}
	public String getKpip() {
		return kpip;
	}
	public void setKpip(String kpip) {
		this.kpip = kpip;
	}
	public String getKtip() {
		return ktip;
	}
	public void setKtip(String ktip) {
		this.ktip = ktip;
	}
	public String getKbip() {
		return kbip;
	}
	public void setKbip(String kbip) {
		this.kbip = kbip;
	}
	public String getKcip() {
		return kcip;
	}
	public void setKcip(String kcip) {
		this.kcip = kcip;
	}
}
